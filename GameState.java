public class GameState {
    private int gs;
    //constructor for GameState
    public GameState(){gs=0;}
    public GameState(int gs) {
        this.gs = gs;
    }
    //getter for gs
    public int getGameState() {
        return gs;
    }
    //setter for gs
    public void setGameState(int gs) {
        this.gs = gs;
    }
}
